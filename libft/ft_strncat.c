#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	size_t	i;
	int		j;

	j = 0;
	i = 0;
	while (s1[j])
		j++;
	while (i < n && s2[i] != '\0')
	{
		s1[j++] = s2[i++];
	}
	s1[j] = '\0';
	return (s1);
}
